package com.sogou.upd.passport.oauth2.common.exception;

public class HTMLTextParseException extends Exception {

	private static final long serialVersionUID = 3644157691675929639L;

	public HTMLTextParseException() {
		super();
	}

	public HTMLTextParseException(String s) {
		super(s);
	}

	public HTMLTextParseException(Throwable throwable) {
		super(throwable);
	}

	public HTMLTextParseException(String s, Throwable throwable) {
		super(s, throwable);
	}
}
